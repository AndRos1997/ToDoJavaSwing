    private void cargarTabla() {
        javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) tblTareas.getModel();
    
    // Limpiar tabla
    modelo.setRowCount(0);

    // Obtener filtro seleccionado
    String filtro = cmbFiltroEstado.getSelectedItem().toString();

    // Recorrer lista
    for (String[] tarea : listaTareas) {
        String estado = tarea[1];

    // Mostrar según filtro
        if (filtro.equals("Todos") || estado.equals(filtro)) {
            modelo.addRow(tarea);
        }
    }
    }
    
