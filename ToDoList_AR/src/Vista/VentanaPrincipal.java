    private void btnEliminarTareaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarTareaActionPerformed
        // TODO add your handling code here:
        //VALIDACION SIN DATOS
        javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) tblTareas.getModel();

        if (modelo.getRowCount() == 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Aún no se ha creado una tarea");
        return;
        }
        
        int fila = tblTareas.getSelectedRow();
        // VALIDACIÓN DE TAREA SELECCIONADA
        if (fila == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Seleccione una tarea para eliminar");
        return;
        }

        // PREGUNTAR TITULO DE LA TAREA
        String titulo = modelo.getValueAt(fila, 0).toString();

        // DUALIDAD DE LA ELIMINACIÓN DE LA TAREA SELECCIONADA
        int opcion = javax.swing.JOptionPane.showConfirmDialog(
                this,
                "Eliminará la tarea: " + titulo + "\n¿Desea continuar?",
                "Confirmar eliminación",
                javax.swing.JOptionPane.YES_NO_OPTION
        );

        // CONFIRMACIÓN DE TAREA ELIMINADA
        if (opcion == javax.swing.JOptionPane.YES_OPTION) {
            listaTareas.remove(fila);
            cargarTabla();
        }
    }//GEN-LAST:event_btnEliminarTareaActionPerformed

    private void btnCambioEstadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCambioEstadoActionPerformed
        // TODO add your handling code here:
        javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) tblTareas.getModel();
        if (modelo.getRowCount() == 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Aún no se ha creado una tarea");
        return;
        }
        
        int fila = tblTareas.getSelectedRow();
        // VALIDACIÓN TAREA SELECCIONADA
        if (fila == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Seleccione una tarea");
        return;
        }
        // OBTENER ESTADO DEL COMBO BOTON
        String nuevoEstado = cmbEstado.getSelectedItem().toString();
        // ACTUALIZAR TABLA
        listaTareas.get(fila)[1] = nuevoEstado;
        cargarTabla();
    }//GEN-LAST:event_btnCambioEstadoActionPerformed

    private void tblTareasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTareasMouseClicked
        // TODO add your handling code here:
        int fila = tblTareas.getSelectedRow();
        if (fila != -1) {
            javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) tblTareas.getModel();
            String estado = modelo.getValueAt(fila, 1).toString();
            cmbEstado.setSelectedItem(estado);
        }
    }//GEN-LAST:event_tblTareasMouseClicked

