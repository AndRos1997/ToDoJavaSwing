    private void btnAgregarTareaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarTareaActionPerformed
        // TODO add your handling code here:
        String titulo = txtTitulo.getText();
        String estado = cmbEstado.getSelectedItem().toString();

        // VALIDACIÓN TITULO OBLIGATORIO
        if (titulo.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "El título es obligatorio");
        return;
        }
        // AGREGAR TAREAS A LA TABLA
        javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) tblTareas.getModel();
        listaTareas.add(new String[]{titulo, estado});//Para manejo de datos
        // Anterior sin guardado modelo.addRow(new Object[]{titulo, estado}); //Visual en la tabla
        cargarTabla();

        // LIMPIAR CAMPOS
        txtTitulo.setText("");
        txtDescripcion.setText("");
        cmbEstado.setSelectedIndex(0);
    }//GEN-LAST:event_btnAgregarTareaActionPerformed

